public class Floyds_Triangle
{
    static void method()
    {
        System.out.println("\f");
        int a=1;
        for(int cnt=1;cnt<=5;cnt++)
        {
            for(int j=1;j<=cnt;j++, a++)
            {
                System.out.print(a+" ");
            }
            System.out.println();
        }
    }
}