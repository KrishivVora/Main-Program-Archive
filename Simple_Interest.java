public class Simple_Interest
{
    static void main(int P,int R, int T)
    {
        float SI=(P*R*T)/100;
        System.out.println("If the Principal is "+P);
        System.out.println("If the Rate is "+R);
        System.out.println("If the Time is "+T);
        System.out.println("Then the Simple Interest is equal to "+SI);
    }
}