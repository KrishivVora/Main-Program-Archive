public class Remainder
{
    static void main(int n)
    {
        int a=n%1;
        int b=n%2;
        int c=n%3;
        int d=n%4;
        int e=n%5;
        int f=n%6;
        int g=n%7;
        int h=n%8;
        int i=n%9;
        System.out.println("Remainder when n is divided by 1 "+a);
        System.out.println("Remainder when n is divided by 2 "+b);
        System.out.println("Remainder when n is divided by 3 "+c);
        System.out.println("Remainder when n is divided by 4 "+d);
        System.out.println("Remainder when n is divided by 5 "+e);
        System.out.println("Remainder when n is divided by 6 "+f);
        System.out.println("Remainder when n is divided by 7 "+g);
        System.out.println("Remainder when n is divided by 8 "+h);
        System.out.println("Remainder when n is divided by 9 "+i);
    }
}