public class Circle//this program find the area and circumference of a circle if radius is given
{
    public static void main(int radius)
    {
        double area= 3.14*radius*radius;
        double circumference= 2*3.14*radius;
        System.out.println("If radius of the circle is "+radius);
        System.out.println("Then Area is equal to "+area);
        System.out.println("Then Circumference is equal to "+circumference);
    }//end of method
}//end of class